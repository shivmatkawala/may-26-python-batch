# Numeric Datatypes:-

    # INT ==> Integer:-
        # An Integer is a number without decimal point.
        # Always a whole number

        # Integer can be +ve or -ve

        # including 0

        # Examples:-
            # 0, 2, 90, -456, 12, -34, 

# FLOAT:- 
    # A number with decimal point is called a float.
    # A float also can be +ve or -ve

    # Examples:-
        # 1.3, 7.6, -86.76, 0.0


# Complex:-
    # A complex number is combination Real number and Imaginary Number.

    # What is Real Number ?
        # All Int and Floats are real number

    # What is Imaginary Number ?
        # A number Suffixed with 'j' is called an Imaginary Number

    # Examples:-    
            # 7+8j ==> Complex Number
            # 7 ==> Real NUmber
            # 8j ==> Imaginary 
            # -2.3+56j
            # 89.06-23.6j

